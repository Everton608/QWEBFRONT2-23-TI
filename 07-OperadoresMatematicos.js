
// Operadores Matemáticos/Aritméticos
/*
Comentário de várias linhas 

+ [SOMA]
- [SUBTRAÇÃO]
* [MULTIPLICAÇÃO]
/ [DIVISÃO]
 
++ [INCREMENTO]
-- [DECREMENTO]
2³ [POTÊNCIA] exemplo: 2 * 2 * 2 = 8 
** [EXPONENCIAÇÃO]
%  [MÓDULO]

*/
let valor1 = 50;
let valor2 = 5;

let soma = valor1 + valor2;
console.log("Resultado da soma " + soma);

let subtracao = valor1 - valor2;
console.log("Resultado da subtração " + subtracao);

let multiplicacao = valor1 * valor2;
console.log("Resultado da multiplicacao " + multiplicacao);

let divisao = valor1 / valor2;
console.log("Resultado da divisao " + divisao);

let incremento = ++valor1;
console.log("Resultado do incremento " + incremento);

let decremento = --valor2;
console.log("Resultado do decremento " + decremento);

let potencia = valor1 ** valor2;
console.log("Resultado da exponenciação " + potencia);

let resto = valor1 % valor2;
console.log("Resultado do resto da divisão " + resto);